public class User {
    private int id;
    private String name;
    Branch[] branches;
    private String Email;
    private String phone;

    public User(int id, String name, Branch[] branches, String email, String phone) {
        this.id = id;
        this.name = name;
        this.branches = branches;
        Email = email;
        this.phone = phone;
    }




}

